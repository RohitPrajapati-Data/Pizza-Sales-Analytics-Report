AS AVG_ORDER_VALUE 
FROM PIZZA_SALES;

-- 4. TOTAL PIZZA SOLD
SELECT SUM(QUANTITY) AS TOTAL_PIZZA_SOLD FROM  pizza_sales;

-- 5. AVERAGE PIZZA PER ORDER
SELECT SUM(QUANTITY)/COUNT(DISTINCT ORDER_ID) FROM PIZZA_SALES;

